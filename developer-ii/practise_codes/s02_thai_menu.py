# s02_thai_menu.py — เมนูเกมภาษาไทยบน rich UI widgets (ฉบับฝึก)
#
# วิธีรัน: เปิดไฟล์นี้ใน BENTO IDE แล้วกด "Program to Device"
#
# ของใหม่คาบนี้ (ต่อยอดจากการวาดสนาม Snake):
#   - game.Text วาดภาษาไทยได้อยู่แล้วสำหรับ HUD/คะแนน (ป้ายธรรมดา)
#   - แต่ "หน้าเมนู" ของเกมจริงอยากได้ปุ่มกด, dropdown เลือกระดับ, checkbox
#     นี่คืองานของโมดูล ui ซึ่ง render ภาษาไทยได้ทุก widget (firmware v1.2.0)
#
# สิ่งที่จะได้ฝึก:
#   - import ui แล้วตั้งผืนจอด้วย ui.screen(เท่ากับขนาดจอเกม)
#   - ui.Label เลือกขนาดฟอนต์ด้วย value= (14/16/20/24/28)
#   - ui.Button / ui.Dropdown / ui.Checkbox ที่มีข้อความเป็นภาษาไทย
#   - อ่านเหตุการณ์การกดด้วย ui.poll() เทียบกับ .id() ของแต่ละ widget
#
# เติมช่องที่มีคำว่า "เติม:" ให้ครบ (มี 2 จุด) แล้วลองกดบนจอจริงดู

import ui
import time

ui.screen(792, 398)     # ตั้งผืนจอให้เท่าจอเกม (game.WIDTH x game.HEIGHT)
ui.clear()              # ล้างของเก่าบนจอก่อนวางเมนูใหม่

# --- หัวเรื่อง: ใช้ value= คุมขนาดฟอนต์ ยิ่งเลขมากตัวยิ่งใหญ่ ---
ui.Label("ตั้งค่าเกมงู", x=40, y=24, color=0xFFFFFF, value=28)   # ตัวใหญ่สุด = หัวเรื่อง
ui.Label("ปรับให้ถูกใจก่อนเริ่มเล่น", x=40, y=70, color=0xAACCFF, value=16)

# --- ป้ายกำกับช่องเลือกระดับความยาก (ฟอนต์กลาง) ---
ui.Label("ระดับความยาก", x=40, y=130, color=0xFFFFFF, value=20)

# Dropdown: ตัวเลือกคั่นด้วย \n (ขึ้นบรรทัดใหม่) ส่งเป็น argument แรกเลย
# 3 ระดับนี้สแต็กกันบนจอได้ถูกต้อง รวมคำที่มีสระซ้อน เช่น "ปานกลาง"
diff_dd = ui.Dropdown("ง่าย\nปานกลาง\nยาก", x=300, y=124)

# --- ตัวเลือกเปิด/ปิดเสียง: ใส่ value=1 ให้ติ๊กไว้ตั้งแต่แรก ---
sound_cb = ui.Checkbox("เปิดเสียงในเกม", x=40, y=190, value=1)

# เติม: เพิ่ม checkbox อีกหนึ่งช่องชื่อ "สั่นเมื่อชน" ที่ x=40, y=235
#       ใบ้: ui.Checkbox("สั่นเมื่อชน", x=40, y=235)  (ยังไม่ต้องใส่ value= = ไม่ติ๊ก)


# --- ปุ่มเริ่มเกม (ข้อความไทยบนปุ่ม) ---
start_btn = ui.Button("เริ่มเกม", x=300, y=300, w=200, h=60)

# เติม: สร้างปุ่ม "ออก" ไว้มุมซ้ายล่างด้วย ui.Button
#       ใบ้: exit_btn = ui.Button("ออก", x=40, y=300, w=120, h=60)
exit_btn = None

# จด id ของแต่ละ widget ไว้เทียบตอนมีคนกด (ui.poll คืน handle ของตัวที่ถูกแตะ)
start_id = start_btn.id()

status = ui.Label("เลือกระดับแล้วกดเริ่มเกม", x=40, y=370, color=0x888888, value=16)

# --- ลูปเมนู: รอจนกว่าจะกด เริ่มเกม หรือ ออก ---
while True:
    for ev in ui.poll():                 # คืน list ของเหตุการณ์ (อาจว่างก็ได้)
        handle = ev.get("handle")        # widget ตัวไหนถูกแตะ
        if handle == start_id:
            status.text("เริ่มเกม! (ต่อไปคือโหลดสนาม Snake)")
        # ถ้าทำปุ่ม "ออก" เสร็จแล้ว ลองเทียบ handle กับ exit_btn.id() เพื่อ break
    time.sleep_ms(50)                    # พักสั้น ๆ ไม่ให้ลูป busy เกินจำเป็น
